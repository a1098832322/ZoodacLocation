package com.dsw.model;
/**（预留）
 * @param id 房间id
 * @param room_name 房间名称
 * @param beginX endX beginY endY 房间四个点的坐标的像素值
 * @author Administrator
 *
 */
public class Room {
	private int id;
	private String roomName;
	private int beginX,endX,beginY,endY;
	public Room(int id, String roomName, int beginX, int endX, int beginY, int endY) {
		super();
		this.id = id;
		this.roomName = roomName;
		this.beginX = beginX;
		this.endX = endX;
		this.beginY = beginY;
		this.endY = endY;
	}
	public Room() {
		super();
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRoomName() {
		return roomName;
	}
	public void setRoomName(String roomName) {
		this.roomName = roomName;
	}
	public int getBeginX() {
		return beginX;
	}
	public void setBeginX(int beginX) {
		this.beginX = beginX;
	}
	public int getEndX() {
		return endX;
	}
	public void setEndX(int endX) {
		this.endX = endX;
	}
	public int getBeginY() {
		return beginY;
	}
	public void setBeginY(int beginY) {
		this.beginY = beginY;
	}
	public int getEndY() {
		return endY;
	}
	public void setEndY(int endY) {
		this.endY = endY;
	}
	
}
